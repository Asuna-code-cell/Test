#include <stdio.h>
int main() {
	int a = 3;
	int b = -5;
	int c = a & b;
	//二进制进行按位的与，在磁盘中按照补码的形式存在，用补码进行运算
	//3：0000000000000000000000000011；
	//-5：原码：1000000---0101；补码1111111111111111---1011  按位与只有两个全为1结果才为1，结果为00000----0011  3

	int d = a | b;
	//有一个1就为1 11111111111111111-1011  原码 10000000000000-0101

	int e = a ^ b;//异或，相同为0，相反为1   111111111111111-1000       原码为100000000000000000000000000-1000  -8        
	printf(" %d\n", c);
	printf(" %d\n", d);
	printf(" %d\n", e);
}